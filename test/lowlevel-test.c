#include "alea/alea.h"
#include "alea/algorithms.h"

#include <unity.h>

#include <string.h>

alea_state *g_state_128;
alea_state *g_state_256;

void setUp(void) {
  uint8_t initial_seed[ALEA_SEED_SIZE_SHAKE256];
  memset(initial_seed, 0, ALEA_SEED_SIZE_SHAKE256);

  g_state_128 = alea_init(initial_seed, ALEA_ALGORITHM_SHAKE128);
  g_state_256 = alea_init(initial_seed, ALEA_ALGORITHM_SHAKE256);
}

void tearDown(void) {
  alea_free(g_state_128);
  alea_free(g_state_256);
}

static void state_init_and_free(void) {
  uint8_t seed[ALEA_SEED_SIZE_SHAKE256];
  memset(seed, 0, ALEA_SEED_SIZE_SHAKE256);

  alea_state *temporary_128 = alea_init(seed, ALEA_ALGORITHM_SHAKE128);
  alea_state *temporary_256 = alea_init(seed, ALEA_ALGORITHM_SHAKE256);

  alea_free(temporary_128);
  alea_free(temporary_256);
}

